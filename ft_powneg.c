#include "libft.h"

double	ft_powneg(int num, int s)
{
	double res;

	if (s < 0)
	{
		s = -s;
		num = ft_pow(num, s);
		res = (1.0 / (double)num);
		return (res);
	}
	else
		return (0.0);
}