#include "libft.h"

long long	ft_numlen(long long n)
{
	long long	s;
	long long	del;

	if (n < -9223372036854775807)
		return (20);
	del = 10;
	s = 1;
	if (n < 0)
	{
		s++;
		n = -n;
	}
	while (n > 9)
	{
		s++;
		n = n / del;
	}
	return (s);
}
