#include "libft.h"

void	ft_putnbr_u(unsigned long long n)
{
	if (n < 10)
	{
		ft_putchar(n + 48);
	}
	else if (n > 9)
	{
		ft_putnbr_u(n / 10);
		ft_putnbr_u(n % 10);
	}
}
