#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		s;

	s = 0;
	i = 0;
	while (s1[s] != '\0')
		s++;
	while (s2[i] != '\0' && i < n)
	{
		s1[s] = s2[i];
		i++;
		s++;
	}
	s1[s] = '\0';
	return (s1);
}
